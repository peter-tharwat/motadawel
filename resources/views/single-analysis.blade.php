							<h3><strong>كيفية تحديد الجمهور المستهدف في 6 خطوات</strong></h3><hr>	
							<h3>1- من هو جمهورك؟</h3>
							<hr>
							<P>عند التفكير فيمن قد يكون جمهورك المستهدف، يجب أن تفكر في الأشخاص الذين يتعرفون على علامتك التجارية.
							أحد الطرق لمعرفة ذلك هي مراقبة من يتابع، ويشارك، ويعلق على منشوراتك على مواقع التواصل الاجتماعي مثل Facebook وLinkedIn وYouTube وInstagram.</p>
							<P>إذا كان شخص ما على استعداد للتفاعل معك على مواقع التواصل الاجتماعي، فمن المحتمل أن يكون من جمهورك المستهدف.</p>
							<img src="assets/img/single-blog1.jpg">
							<P>ولكن في كثير من الحالات، قد لا يكون جمهورك المثالي دائمًا على شبكة التواصل الاجتماعي. قد يكونون غير نشطين على وسائل التواصل الاجتماعي لكنهم يشترون من شركتك بشكل متكرر أو يشتركون في خدماتك.
							حتى أولئك الذين اشتروا منك مرة واحدة فقط يجب اعتبارهم جزءًا من جمهورك المستهدف، لأن الشخص الذي اشترى مرة واحدة قد يشتري مرة أخرى.</p>
							<p>لا جدوى من بذل جهد كبير للبيع لعملاء جدد إذا لم تبذل جهدًا مماثلًا للحفاظ على العملاء الذين اكتسبتهم بالفعل.
							يحب العملاء أن يشعروا بأنهم مميزون، وهذا هو سبب أهمية عملية ما بعد البيع. يجب أن تظل علاقتك بالعميل قائمة حتى بعد عملية الشراء.</p>
							<h3>2- ما هي أكبر الصعوبات أو المشاكل أو الرغبات التي لديهم؟</h3>
							<hr>
							<P>عند تحديد الجمهور المستهدف، اعلم ان ما هو رائع وممتع وجيد بالنسبة لك قد لا يكون كذلك بالنسبة للعميل.
							لا يمكنك التفكير في نفسك فقط عندما يحين الوقت لتحديد صعوبات ومشاكل ورغبات جمهورك المستهدف. يجب أن تضع نفسك في مكانهم.</p>
							<P>إذا كان شخص ما على استعداد للتفاعل معك على مواقع التواصل الاجتماعي، فمن المحتمل أن يكون من جمهورك المستهدف.</p>
							<img src="assets/img/single-blog2.jpg">
							<p>لا تقدم عروض بناءً على ما تعتقده. اجعلها وفقًا لبحث قائم على البيانات والتجارب السابقة وتحليل سلوك العملاء المحتملين.
							افهم أكبر الصعوبات التي يواجها جمهورك لتحاول مساعدتهم في حلها.</p>
							<h3>3- أين يجدون المعلومات التي يحتاجونها يوميًا؟</h3>
							<hr>
							<P>كل شخص يحتاج إلى معلومات.</p>
							<P>كل يوم تأتيك الكثير من المعلومات من القنوات التي تتابعها، ولكن عندما تكون في أمس الحاجة إليها، أين تذهب للعثور على تلك المعلومات؟</p>
							<P>حدد قنوات الاتصال الأنسب لجمهورك المستهدف وحاول التحدث معهم باستخدام اللغة التي يتحدثون بها.
							على سبيل المثال، قد يقرأ جمهورك المستهدف على المدونات أو يقضون الكثير من الوقت على المواقع الاجتماعية مثل YouTube وLinkedIn ليحصلوا على تلك المعلومات.</p>
							<h3>4- ما هي فائدة منتجك؟</h3>
							<hr>
							<P>الكل يريد حلولاً لمشاكلهم وجعل حياتهم أسهل. هذه رغبة جماعية ولا تختلف عن جمهورك المستهدف.
							عندما تقوم بتحديد الجمهور المستهدف، فكر قليلاً في منتجك ومشكلة جمهورك المستهدف. ما هي الفوائد التي يقدمها منتجك أو خدمتك؟ ماذا يمكنك أن تفعل لحل تلك المشاكل؟ هل تعلم القيمة الرئيسي التي تقدمها لجمهورك؟</p>
							<P>مع وجود الكثير من المنافسة، يجب أن تحاول العثور على ميزتك التنافسية في مكانتك وتحاول دائمًا تحسين منتجك، وتقديم شيء إضافي لا يفعله الآخرون.</p>
							<h3>5- ما الذي يلفت انتباههم سلباً؟</h3>
							<hr>
							<P>يساعد التفاؤل كثيرًا، لكن التفكير في السلبيات يمكن أن يساعد أيضًا، خاصة عندما نتحدث عن الجمهور المستهدف.
								أفضل من التفكير فيما يريده جمهورك، يمكنك التفكير فيما لا يريده، وما يعتبره سلبيًا، وما يتجنبه بشكل مستمر.
							مع وجود هذه المعلومات القوية في متناول يدك عندما تقوم بتحديد الجمهور المستهدف، قد يكون لديك المزيد من الفرص لجذب عملائك المحتملين.</p>
							<P>إن تجنب ما يعتبرونه سلبي هو الخطوة الأولى لكسب موافقتهم. بعد ذلك، كل ما عليك فعله هو تطبيق استراتيجيات أخرى للقيام بتسويق فعّال.</p>
								
							<h3>6- بمن يثقون؟</h3>
							<hr>
							<P>الثقة هي كل شيء لجمهورك المستهدف. لا أحد يشتري منتجًا أو خدمة من شركة لا يعرفها أو يثق بها.</p>
							<P>هذا هو سبب قراءة المراجعات على Amazon وهي مهمة جدًا للبائعين. إنهم يعلمون أنها تبني الثقة، كما أنها تساعد أمازون في أن تصبح شركة بقيمة تريليون دولار.</p>
							<img src="assets/img/single-blog3.jpg">
							<p>على الرغم من أن هذا هو السؤال الأخير من أجل تحديد الجمهور المستهدف، إلا أنه أحد أهم الأسئلة.
							هذا هو سبب أهمية سمعة شركتك. يعد الاهتمام بالعلاقة مع عملائك أمرًا ضروريًا لأنهم سينشرون المعلومات حول علامتك التجارية على الإنترنت وإلى أصدقائهم وعائلاتهم.</p>
							<p>إذا حصلت على تقييمات جيدة، ولديك تعليقات إيجابية، وحصلت على سمعة طيبة، فستكون هذه القاعدة للعملاء المحتملين ليشعروا بالحافز للشراء منك.</p>
							<h3>الخلاصة</h3>
							<hr>
							<P>تحديد الجمهور المستهدف هو جزء مهم لنجاح مشروعك. بمجرد أن تعرف من تستهدفه، يصبح من الأسهل بكثير معرفة الوسائط التي يمكنك استخدامها للوصول إليهم والرسائل التسويقية التي سيكون لها صدى معهم. فبدلاً من إرسال بريد مباشر إلى كل شخص في قائمة البريد لديك، يمكنك إرساله فقط إلى الأشخاص الذين يناسبون معاييرك. بذلك توفر المال وتحصل على عائد استثمار أفضل من خلال تحديد جمهورك المستهدف.</p>
